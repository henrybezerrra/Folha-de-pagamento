package pacoteprincipal;

/**
 *
 * @author Murilo Henrique
 */
public class Atributos {

    private double salFamilia; // Salario Familia
    private double sal; // Salario
    private double qf; // Quantidade de filho
    private double inss; // INSS
    private double irrf; // IRRF
    private double salLiquido; // INSS
    private double irrfAli; // IRRF

    public double getSalFamilia() {
        return salFamilia;
    }

    public void setSalFamilia(double salFamilia) {
        this.salFamilia = salFamilia;
    }

    public double getSal() {
        return sal;
    }

    public void setSal(double sal) {
        this.sal = sal;
    }

    public double getQf() {
        return qf;
    }

    public void setQf(double qf) {
        this.qf = qf;
    }

    public double getInss() {
        return inss;
    }

    public void setInss(double inss) {
        this.inss = inss;
    }

    public double getIrrf() {
        return irrf;
    }

    public void setIrrf(double irrf) {
        this.irrf = irrf;
    }

    public double getSalLiquido() {
        return salLiquido;
    }

    public void setSalLiquido(double salLiquido) {
        this.salLiquido = salLiquido;
    }

    public double getIrrfAli() {
        return irrfAli;
    }

    public void setIrrfAli(double irrfAli) {
        this.irrfAli = irrfAli;
    }

}
